
public class Main {
	public static void main(String[] args) {
		// Lógicos
		boolean booleano = true;
		System.out.println("Booleano:\t" + booleano);

		// Caracter
		char caracter = 'A';
		System.out.println("Caracter:\t" + caracter);

		// Numéricos enteros
		byte numByte = 100;
		System.out.println("Byte:\t\t" + numByte);

		short numShort = 10000;
		System.out.println("Short:\t\t" + numShort);

		int numInt = 1000000;
		System.out.println("Int:\t\t" + numInt);

		long numLong = 1000000000L;
		System.out.println("Long:\t\t" + numLong);

		// Numéricos reales
		float numFloat = 10.5f;
		System.out.println("Float:\t\t" + numFloat);

		double numDouble = 10.5555555;
		System.out.println("Double:\t\t" + numDouble);
	}
}
