
public class Main {
	public static void main(String[] args) {

		int numero = 25;
		System.out.println("Valor inicial: \t" + numero);

		// 1)
		numero += 5;
		System.out.println("Sumar 5: \t" + numero);

		// 2)
		numero -= 15;
		System.out.println("Restar 15: \t" + numero);

		// 3)
		numero *= 2;
		System.out.println("Mltiplicar: \t" + numero);

		// 4)
		numero /= 2;
		System.out.println("Dividir: \t" + numero);

		// 5)
		numero %= 3;
		System.out.println("Dividir: \t" + numero);
	}
}
